package com.framework.utils;

import com.framework.constants.AnnotationConstants;

public class URLUtils {
	public static String generateURLforUserInfo(String path) {
		return AnnotationConstants.ABSOLUTE_URL + path + "/userInfo";
	}
	public static String generateURLforAnnotation(String path){
		return AnnotationConstants.ABSOLUTE_URL + path + "/annotation";
	}
	public static String generateURLforComment(String path){
		return AnnotationConstants.ABSOLUTE_URL + path + "/comment";
	}
	public static String generateURLforHomework(String path) {
		return AnnotationConstants.ABSOLUTE_URL + path + "/homework";
	}
	public static String generateURLforBookCover(String path) {
		return AnnotationConstants.UPLOAD_URL + path + "/bookcover";
	}
}
