package com.annotation.entityview;

import java.util.Date;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;

@Entity
@Table(name = "ClassStudentTeacherView")
public class ClassStudentTeacherV {
	
	public static final Integer TABLE_CODE = 10000;
	
	@Id
	@Column(name = "STUDENT_ID")
	private Integer studentId;
	
	@Column(name = "STUDENT_CODE")
	private String studentCode;
	
	@Column(name = "STUDENT_NAME")
	private String studentName;
	
	@Column(name = "STUDENT_GENDER")
	private String studentGender;
	
	@Column(name = "CLASS_ID")
	private Integer classId;
	
	@Column(name = "CLASS_NAME")
	private String className;
	
	@Column(name = "TEACHER_ID")
	private Integer teacherId;
	
	@Column(name = "ORGANIZATION_ID")
	private Integer organizationId;

	@Column(name = "STUDENT_CREATIONTIME")
	private Date studentCreationtime;
	
	public Integer getStudentId() {
		return studentId;
	}

	public void setStudentId(Integer studentId) {
		this.studentId = studentId;
	}

	public String getStudentCode() {
		return studentCode;
	}

	public void setStudentCode(String studentCode) {
		this.studentCode = studentCode;
	}

	public String getStudentName() {
		return studentName;
	}

	public void setStudentName(String studentName) {
		this.studentName = studentName;
	}

	public String getStudentGender() {
		return studentGender;
	}

	public void setStudentGender(String studentGender) {
		this.studentGender = studentGender;
	}

	public String getClassName() {
		return className;
	}

	public void setClassName(String className) {
		this.className = className;
	}

	public Integer getTeacherId() {
		return teacherId;
	}

	public void setTeacherId(Integer teacherId) {
		this.teacherId = teacherId;
	}

	public Integer getOrganizationId() {
		return organizationId;
	}

	public void setOrganizationId(Integer organizationId) {
		this.organizationId = organizationId;
	}

	public Integer getClassId() {
		return classId;
	}

	public void setClassId(Integer classId) {
		this.classId = classId;
	}

	public Date getStudentCreationtime() {
		return studentCreationtime;
	}

	public void setStudentCreationtime(Date studentCreationtime) {
		this.studentCreationtime = studentCreationtime;
	}

	
	
	
}